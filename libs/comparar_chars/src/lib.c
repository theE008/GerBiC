// Retorna a diferença encontrada.
int comparar_chars (char* a, char* b)
{
    erro_se_nulo (a);
    erro_se_nulo (b);

    int tam_a = tamanho_chars (a);
    int tam_b = tamanho_chars (b);

    int resposta = 0;

    if (tam_a != tam_b) resposta = tam_a - tam_b;
    else for (int x = 0; x < tam_a && !resposta; x ++)
        resposta = a [x] - b [x];

    return resposta;
}

