// Retorna a diferença encontrada.
size_t compararTexto (const char* a, const char* b)
{
    ERRO_SE_NULO (a);
    ERRO_SE_NULO (b);

    size_t tam_a = tamanhoTexto (a);
    size_t tam_b = tamanhoTexto (b);

    size_t resposta = 0;

    if (tam_a != tam_b) resposta = tam_a - tam_b;
    else for (size_t x = 0; x < tam_a && !resposta; x ++)
        resposta = a [x] - b [x];

    return resposta;
}

