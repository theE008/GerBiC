// Inverte o chars
void inverterTexto (char* txt)
{
    ERRO_SE_NULO (txt);

    size_t tam = tamanhoTexto (txt);
    size_t half = tam/2;

    for (size_t x = 0; x < half; x++)
    {
        size_t y = tam - x - 1;

        char c = txt [y]; // Swap
        txt [y] = txt [x];
        txt [x] = c;
    }
}

